package BitwiseOperators;

public class BitwiseOperator {
    static void main() {

        int a = 5;
        int b = 6;
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~a);
        System.out.println(~b);
        System.out.println(a<<3);
        System.out.println(b>>2);

    }
}
